s = "racecarx"

def validPalindromeHelper(s, left, right):
    while left < right: 
        if s[left] != s[right]:
            return False
        left += 1
        right -= 1
    return True

def validPalindromeTwo(s):
    left = 0
    right = len(s) - 1
    while left < right:
        return validPalindromeHelper(s, left + 1, right) or validPalindromeHelper(s, left, right - 1)
